import Api from "./baseApi";
export const getMessageInboxHash = (data) => {
  return Api.post(`/chat/inboxHash`, data)
    .then((response) => {
      // Xử lý dữ liệu trả về từ response ở đây
      return response.data;
    })
    .catch((error) => {
      // Xử lý lỗi ở đây
      throw error;
    });
};

export const sendMessage = (data) => {
  return Api.post(`/chat`, data)
    .then((response) => {
      // Xử lý dữ liệu trả về từ response ở đây
      return response.data;
    })
    .catch((error) => {
      // Xử lý lỗi ở đây
      throw error;
    });
};
