import Api from "./baseApi";

export const createUserPost = (post) => {
  return Api.post("/userpost", post)
    .then((response) => {
      // Xử lý dữ liệu trả về từ response ở đây
      return response.data;
    })
    .catch((error) => {
      // Xử lý lỗi ở đây
      throw error;
    });
};

export const getUserPostByOwnerID = (ownerID) => {
  return Api.get(`/userpost/owner/${ownerID}`)
    .then((response) => {
      // Xử lý dữ liệu trả về từ response ở đây
      return response.data;
    })
    .catch((error) => {
      // Xử lý lỗi ở đây
      throw error;
    });
};
